class Activity extends MY_Controller
{
    function __construct()
    {
        parent::__construct();
        $this->load->model(['Activity_model', 'Audit_model']);
        $this->load->library('form_validation');
        $this->load->library('datatables');
    }

    public function index()
    {
        // $page_data = $this->Activity_model->get_all();
        $page_data = $this->Activity_model->get_all();

        $data = array(
            'title' => 'Activities',
            'activity_data' => $page_data,
        );

        $this->template->load('admin', 'activity/list', $data);

    }

    public function delete($id)
    {
      //  $this->check_role(); 
        $row = $this->Activity_model->get_by_id($id);

        if ($row) {
            $this->Activity_model->delete($id);
            $task = 'Deleted a activity from activities table';
            $this->audit($task);
            $this->session->set_flashdata('message', 'activity deleted Successfully');
            redirect(site_url('activity'));
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('activity'));
        }
    }

    public function audit($task)
    {
        $login = $this->session->userdata('logged_in');
        $user = $login['firstname'] . ' ' . $login['lastname'];
        $data_audit = array(
            'user' => $user,
            'task' => $task,
            'date_time' => date('Y-m-d H:i:s', time()),
        );

        $this->Audit_model->insert($data_audit);
    }

    public function _rules()
    {
        $this->form_validation->set_rules(
            'name',
            'name',
            'trim|required'
        );
        $this->form_validation->set_rules('email', 'Email', 'trim|required');
    }

}

